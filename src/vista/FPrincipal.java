    /**
     * Método que sirve para activar o desactivar ciertos botones los cuales
     * cambiaran dependiendo del tipo de usuario que haya iniciado sesión
     *
     * @param tipo Tipo de usuario a establecer.
     * @param usuario Uusario a establecer.
     */
    public void botones(int tipo, Usuario usuario) {
        this.usuarioR = usuario;
//      this.jPanelTransporte.setVisible(true);
        this.jButtonRegistrarUsuarios.setEnabled(true);
        this.jButtonRegistrarEmpresas.setEnabled(true);
        this.jButtonAsignarTraslados.setEnabled(true);
        this.jButtonRegistrarResiduos.setEnabled(true);
        this.jButtonRegistrarQuimicos.setEnabled(true);
        this.jButtonRegistrarSolicitarTraslado.setEnabled(true);
        this.jButtonRegistrarTraslado.setEnabled(true);
        this.jButtonRegistrarVehiculos.setEnabled(true);
        switch (tipo) {
            case 1:
//              this.jPanelTransporte.setVisible(false);
                this.jButtonRegistrarResiduos.setEnabled(false);
                this.jButtonRegistrarQuimicos.setEnabled(false);
                this.jButtonRegistrarSolicitarTraslado.setEnabled(false);
                this.jButtonRegistrarTraslado.setEnabled(false);
                this.jButtonRegistrarVehiculos.setEnabled(false);
                break;
            case 2:
//                this.jPanelTransporte.setVisible(false);
                this.jButtonRegistrarUsuarios.setEnabled(false);
                this.jButtonRegistrarEmpresas.setEnabled(false);
                this.jButtonAsignarTraslados.setEnabled(false);
                this.jButtonRegistrarTraslado.setEnabled(false);
                this.jButtonRegistrarVehiculos.setEnabled(false);
                break;
            default:
//               this.jPanelProductor.setVisible(false);
                this.jButtonRegistrarUsuarios.setEnabled(false);
                this.jButtonRegistrarEmpresas.setEnabled(false);
                this.jButtonAsignarTraslados.setEnabled(false);
                this.jButtonRegistrarResiduos.setEnabled(false);
                this.jButtonRegistrarQuimicos.setEnabled(false);
                this.jButtonRegistrarSolicitarTraslado.setEnabled(false);
                break;
        }
    }

