    /**
     * Primero comprobara que existan aasignaciones de traslado para la empresa
     * en la que trabaja el usuario, si no existen mostrará un mensaje
     * advirtiendo de ello, si si existen, entonces esconderá esta pantalla y
     * abrirá la pantalla de Registro Traslado e inicializará al usuario y las
     * asignaciones
     *
     * @param evt Evento action perfomed del boton Registrar QTraslado
     */
    private void jButtonRegistrarTrasladoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarTrasladoActionPerformed
        this.usuarioR = FabricaNegocios.crearFNegocio().getUsuario(2, usuarioR.getNombreUsuario(), usuarioR.getContrasena());
        ObjectId idEmpresaTransporte = ((Transportador) this.usuarioR).getIdEmpresaTransporte();
        List<Asignacion_Traslado> asignaciones = this.negocio.consultarAsignacionTrasladosEmpresaT(idEmpresaTransporte);

        if (asignaciones.isEmpty()) {
            this.mostrarMensajeError("No existen asignaciones de traslado para la empresa a la que pertenece.");
            return;
        }

        this.setVisible(false);
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                FRegistroTraslado p = FRegistroTraslado.getInstance();
                p.setUsuario(null);
                p.setUsuario(usuarioR);
                p.mostrarAsignaciones(asignaciones);
                p.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosing(java.awt.event.WindowEvent e) {
                        setVisible(true);
                    }
                });
                p.setVisible(true);
            }
        });

    }//GEN-LAST:event_jButtonRegistrarTrasladoActionPerformed
